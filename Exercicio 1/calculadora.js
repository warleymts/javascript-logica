let firstNumber = Number(prompt("Insira um número de 0 a 99"));

let operation = prompt("Insira uma operação: +, -, *, /");

let secondNumber = Number(prompt("Insira outro número de 0 a 99"));


if ((operation === "+")) alert(firstNumber + secondNumber);
else if ((operation === "-")) alert(firstNumber - secondNumber);
else if ((operation === "*")) alert(firstNumber * secondNumber);
else if ((operation === "/")) alert(firstNumber / secondNumber);
